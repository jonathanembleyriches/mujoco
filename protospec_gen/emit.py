OUT_DIR = os.path.join(ROOT, "cpp", "generated")

BANNER = "// Generated by protospec_gen.emit — do not edit."

# C++ keywords / context-sensitive identifiers that cannot be used verbatim as
# member, enumerator, or type identifiers; sanitized by appending '_'.
_CPP_KEYWORDS = frozenset(
    """
    alignas alignof and and_eq asm atomic_cancel atomic_commit atomic_noexcept
    auto bitand bitor bool break case catch char char8_t char16_t char32_t
    class compl concept const consteval constexpr constinit const_cast continue
    co_await co_return co_yield decltype default delete do double dynamic_cast
    else enum explicit export extern false float for friend goto if inline int
    long mutable namespace new noexcept not not_eq nullptr operator or or_eq
    private protected public reflexpr register reinterpret_cast requires return
    short signed sizeof static static_assert static_cast struct switch
    synchronized template this thread_local throw true try typedef typeid
    typename union unsigned using virtual void volatile wchar_t while xor xor_eq
    override final
    """.split()
)

_PRIM_CPP = {
    "int32": "int32_t",
    "uint64": "uint64_t",
    "float": "float",
    "double": "double",
    "bool": "bool",
    "string": "std::string",
}

_CARDINALITY_CPP = {
    "zero_or_more": "ZeroOrMore",
    "zero_or_one": "ZeroOrOne",
    "one": "One",
}

# Child lists whose XML tag diverges from the child element's own tag. MJCF's
# <worldbody> validates against the <body> row (plan Section 2): it is a Body
# element written under the tag "worldbody" rather than "body". The child element
# (Body) declares tag "body", so this one edge needs an explicit override; every
# other child list's tag is the child element's own tag. Keyed by
# (element name, child-list name).
_CHILD_XML_OVERRIDE = {("Model", "worldbody"): "worldbody"}


def ident(name: str) -> str:
    """A legal C++ identifier for an IDL name (keyword-safe)."""
    return name + "_" if name in _CPP_KEYWORDS else name


# --------------------------------------------------------------------------- #
# Schema index                                                                 #
# --------------------------------------------------------------------------- #
class Schema:
    """Indexed view of the JSON AST for the emitters."""

    def __init__(self, doc: dict):
        self.doc = doc
        self.enums = doc["enums"]
        self.structs = doc["structs"]
        self.variants = doc["variants"]
        self.unions = doc["unions"]
        self.elements = doc["elements"]
        self.enum_by_name = {e["name"]: e for e in self.enums}
        self.struct_by_name = {s["name"]: s for s in self.structs}
        self.variant_by_name = {v["name"]: v for v in self.variants}
        self.union_by_name = {u["name"]: u for u in self.unions}
        self.element_by_name = {e["name"]: e for e in self.elements}

    def elem_xml(self, elem: dict) -> str:
        return elem.get("annotations", {}).get("xml", elem["name"].lower())

    def field_xml(self, f: dict) -> str:
        return f.get("annotations", {}).get("xml", f["name"])

    def child_xml(self, elem: dict, child: dict) -> str:
        """The XML tag routing a child list. For a union child list this is empty
        (dispatch is by member element tag). Otherwise it is the child element's
        own tag, save the worldbody divergence recorded in _CHILD_XML_OVERRIDE."""
        key = (elem["name"], child["name"])
        if key in _CHILD_XML_OVERRIDE:
            return _CHILD_XML_OVERRIDE[key]
        if child.get("union") is not None:
            return ""
        return self.elem_xml(self.element_by_name[child["element"]])


# --------------------------------------------------------------------------- #
# Type mapping                                                                 #
# --------------------------------------------------------------------------- #
def value_type(t: dict) -> str:
    """C++ type of a field value (before optional wrapping)."""
    kind = t["kind"]
    if kind == "prim":
        base = _PRIM_CPP[t["prim"]]
        arity = t.get("arity")
        if arity is None:
            return base
        if arity["kind"] == "fixed":
            return f"std::array<{base}, {arity['size']}>"
        if arity["kind"] == "range":
            return f"ps::InlineVec<{base}, {arity['max']}>"
        return f"std::vector<{base}>"
    if kind == "ref":
        return f"ps::Ref<{t['target']}>"
    if kind == "variant":
        return t["target"]
    if kind == "named":
        # enum scalar or enum keyword set (structs appear only as variant arms).
        name = t["name"]
        if t.get("arity") is not None:  # unbounded keyword set
            return f"std::vector<{name}>"
        return name
    raise AssertionError(f"unhandled type kind {kind!r}")


def stored_type(f: dict, *, in_struct: bool) -> str:
    """Storage type of a field: opt-wrapped for optional element fields; plain
    for required element fields and for all struct (POD-cluster) fields."""
    vt = value_type(f["type"])
    if in_struct:
        return vt
    return f"ps::opt<{vt}>" if f["optional"] else vt


# --------------------------------------------------------------------------- #
# Default value rendering                                                      #
# --------------------------------------------------------------------------- #
def _fmt_scalar(value, prim: str) -> str:
    if isinstance(value, bool):
        return "true" if value else "false"
    if prim in ("int32", "uint64"):
        return str(int(value))
    if prim == "float":
        return f"{float(value)}f"
    return f"{float(value)}"  # double


def render_default(f: dict) -> str:
    """C++ expression for a field's IDL default (the value, not opt-wrapped)."""
    d = f["default"]
    t = f["type"]
    if d["kind"] == "enum":
        return f"{t['name']}::{ident(d['member'])}"
    if d["kind"] == "scalar":
        return _fmt_scalar(d["value"], t["prim"])
    # array default
    base = t["prim"]
    arity = t["arity"]
    elems = ", ".join(_fmt_scalar(v, base) for v in d["values"])
    cpp_base = _PRIM_CPP[base]
    if arity["kind"] == "fixed":
        return f"std::array<{cpp_base}, {arity['size']}>{{{{{elems}}}}}"
    if arity["kind"] == "range":
        return f"ps::InlineVec<{cpp_base}, {arity['max']}>{{{elems}}}"
    return f"std::vector<{cpp_base}>{{{elems}}}"


# --------------------------------------------------------------------------- #
# Reflection classification                                                    #
# --------------------------------------------------------------------------- #
def field_kind(t: dict) -> str:
    kind = t["kind"]
    if kind == "prim":
        return {
            "bool": "Bool",
            "int32": "Int32",
            "uint64": "Uint64",
            "float": "Float",
            "double": "Double",
            "string": "String",
        }[t["prim"]]
    if kind == "ref":
        return "Ref"
    if kind == "variant":
        return "Variant"
    return "Enum"  # named enum (scalar or keyword set)


def field_arity(t: dict) -> tuple[str, int, int]:
    """(ArityKind, min, max) for reflection."""
    arity = t.get("arity")
    if arity is None:
        return ("Scalar", 0, 0)
    if arity["kind"] == "fixed":
        return ("Fixed", arity["size"], arity["size"])
    if arity["kind"] == "range":
        return ("Range", arity["min"], arity["max"])
    return ("Unbounded", 0, 0)


def field_type_name(t: dict) -> str:
    kind = t["kind"]
    if kind == "prim":
        return t["prim"]
    if kind == "ref":
        return t["target"]
    if kind == "variant":
        return t["target"]
    return t["name"]  # named enum


# --------------------------------------------------------------------------- #
# Emitters                                                                     #
# --------------------------------------------------------------------------- #
def emit_types_h(s: Schema) -> str:
    o: list[str] = []
    w = o.append
    w(BANNER)
    w("#ifndef PROTOSPEC_GENERATED_TYPES_H")
    w("#define PROTOSPEC_GENERATED_TYPES_H")
    w("")
    w("#include <array>")
    w("#include <cstdint>")
    w("#include <memory>")
    w("#include <string>")
    w("#include <variant>")
    w("#include <vector>")
    w("")
    w('#include "protospec/core.h"')
    w("")
    w("namespace ps::mjcf {")
    w("")

    # Forward declarations of every element (union item wrappers hold pointers
    # to these, and child lists are vectors of unique_ptr to them).
    w("// Forward declarations.")
    for e in s.elements:
        w(f"struct {e['name']};")
    w("")

    # Enums.
    w("// --- Enums (MJCF keyword tables live in keywords.h) ---")
    for e in s.enums:
        w(f"enum class {e['name']} {{")
        for m in e["members"]:
            w(f"  {ident(m['name'])},")
        w("};")
        w("")

    # POD structs.
    w("// --- POD structs (variant arms, no identity) ---")
    for st in s.structs:
        fields = st["fields"]
        parts = []
        for f in fields:
            parts.append(f"{stored_type(f, in_struct=True)} {ident(f['name'])} = {{}};")
        body = " ".join(parts)
        w(f"struct {st['name']} {{ {body}")
        w(
            f"  friend bool operator==(const {st['name']}&, const {st['name']}&) "
            "= default;"
        )
        w("};")
        w("")

    # Variant aliases.
    w("// --- Variant aliases (DR-3 aliasing groups) ---")
    for v in s.variants:
        arms = ", ".join(value_type(a["type"]) for a in v["arms"])
        w(f"using {v['name']} = std::variant<{arms}>;")
    w("")

    # Union item wrappers (one ordered heterogeneous child list per union).
    w("// --- Union child-list item wrappers (Section 6 interleave) ---")
    for u in s.unions:
        members = u["members"]
        w(f"struct {u['name']} {{")
        w("  enum class Kind {")
        for m in members:
            w(f"    {m},")
        w("  };")
        variant_alts = ", ".join(f"std::unique_ptr<{m}>" for m in members)
        w(f"  std::variant<{variant_alts}> node;")
        w("  Kind kind() const { return static_cast<Kind>(node.index()); }")
        w("};")
        w("")

    # Element structs.
    w("// --- Elements ---")
    for e in s.elements:
        w(f"struct {e['name']} {{")
        w("  ps::SourceLoc loc;")
        w("  std::uint64_t serial = ps::detail::next_serial();")
        for f in e["fields"]:
            w(f"  {stored_type(f, in_struct=False)} {ident(f['name'])} = {{}};")
        for c in e["children"]:
            if c.get("union") is not None:
                w(f"  std::vector<{c['union']}> {ident(c['name'])};")
            else:
                w(
                    f"  std::vector<std::unique_ptr<{c['element']}>> "
                    f"{ident(c['name'])};"
                )
        w("};")
        w("")

    # ElementType enum + kind constant.
    w("// --- Element type ids (reflection / binding) ---")
    w("enum class ElementType {")
    for e in s.elements:
        w(f"  {e['name']},")
    w("};")
    w("")
    w("template <class T> struct element_type_of;")
    for e in s.elements:
        w(
            f"template <> struct element_type_of<{e['name']}> "
            f"{{ static constexpr ElementType value = ElementType::{e['name']}; }};"
        )
    w("")

    # Clone + operator== declarations (defined in types.cc).
    w("// --- Deep clone + field-wise equality (defined in types.cc) ---")
    for u in s.unions:
        w(f"{u['name']} Clone(const {u['name']}& src);")
        w(
            f"bool operator==(const {u['name']}& a, const {u['name']}& b);"
        )
    for e in s.elements:
        w(f"std::unique_ptr<{e['name']}> Clone(const {e['name']}& src);")
        w(f"bool operator==(const {e['name']}& a, const {e['name']}& b);")
        w(
            f"inline bool operator!=(const {e['name']}& a, const {e['name']}& b) "
            "{ return !(a == b); }"
        )
    w("")

    w("}  // namespace ps::mjcf")
    w("")
    w("#endif  // PROTOSPEC_GENERATED_TYPES_H")
    return "\n".join(o) + "\n"


def _field_eq_expr(f: dict) -> str:
    m = ident(f["name"])
    return f"a.{m} == b.{m}"


def emit_types_cc(s: Schema) -> str:
    o: list[str] = []
    w = o.append
    w(BANNER)
    w('#include "types.h"')
    w("")
    w("#include <type_traits>")
    w("#include <utility>")
    w("#include <variant>")
    w("")
    w("namespace ps::mjcf {")
    w("")

    # Union clone + equality.
    for u in s.unions:
        name = u["name"]
        w(f"{name} Clone(const {name}& src) {{")
        w(f"  {name} out;")
        w("  std::visit(")
        w("      [&](const auto& p) {")
        w("        if (p) out.node = Clone(*p);")
        w("      },")
        w("      src.node);")
        w("  return out;")
        w("}")
        w("")
        w(f"bool operator==(const {name}& a, const {name}& b) {{")
        w("  if (a.node.index() != b.node.index()) return false;")
        w("  return std::visit(")
        w("      [&](const auto& pa) {")
        w(
            "        const auto& pb = "
            "std::get<std::decay_t<decltype(pa)>>(b.node);"
        )
        w("        const bool ha = static_cast<bool>(pa);")
        w("        const bool hb = static_cast<bool>(pb);")
        w("        if (ha != hb) return false;")
        w("        return !ha || (*pa == *pb);")
        w("      },")
        w("      a.node);")
        w("}")
        w("")

    # Element clone + equality.
    for e in s.elements:
        name = e["name"]
        w(f"std::unique_ptr<{name}> Clone(const {name}& src) {{")
        w(f"  auto out = std::make_unique<{name}>();")
        w("  out->loc = src.loc;")
        for f in e["fields"]:
            m = ident(f["name"])
            w(f"  out->{m} = src.{m};")
        for c in e["children"]:
            m = ident(c["name"])
            if c.get("union") is not None:
                w(f"  for (const auto& item : src.{m})")
                w(f"    out->{m}.push_back(Clone(item));")
            else:
                w(f"  out->{m} = ps::PtrVecClone(src.{m});")
        w("  return out;")
        w("}")
        w("")

        w(f"bool operator==(const {name}& a, const {name}& b) {{")
        conds = [_field_eq_expr(f) for f in e["fields"]]
        for c in e["children"]:
            m = ident(c["name"])
            if c.get("union") is not None:
                conds.append(f"a.{m} == b.{m}")
            else:
                conds.append(f"ps::PtrVecEq(a.{m}, b.{m})")
        _emit_bool_and(w, conds)
        w("}")
        w("")

    w("}  // namespace ps::mjcf")
    return "\n".join(o) + "\n"


def _emit_bool_and(w, conds: list[str]) -> None:
    if not conds:
        w("  return true;")
        return
    if len(conds) == 1:
        w(f"  return {conds[0]};")
        return
    w(f"  return {conds[0]} &&")
    for c in conds[1:-1]:
        w(f"         {c} &&")
    w(f"         {conds[-1]};")


def emit_visit_h(s: Schema) -> str:
    o: list[str] = []
    w = o.append
    w(BANNER)
    w("//")
    w("// Generated Visit(elem, V&&) hook (plan Section 3's Serialize hook). One")
    w("// traversal, N consumers: V receives every field by id + name + typed")
    w("// reference, and every child list. V's expected surface:")
    w("//   template <class T> void field(int id, const char* name, T& value);")
    w("//   template <class T> void child(int id, const char* name,")
    w("//       std::vector<std::unique_ptr<T>>& list);")
    w("//   template <class U> void union_child(int id, const char* name,")
    w("//       std::vector<U>& list);")
    w("// A tree walk is a V whose child/union_child call Visit on each item.")
    w("#ifndef PROTOSPEC_GENERATED_VISIT_H")
    w("#define PROTOSPEC_GENERATED_VISIT_H")
    w("")
    w("#include <utility>")
    w("#include <variant>")
    w("")
    w('#include "types.h"')
    w("")
    w("namespace ps::mjcf {")
    w("")

    def emit_struct_visit(name: str, fields: list[dict], child_kinds: list):
        for const in ("", "const "):
            w(f"template <class V> void Visit({const}{name}& e, V&& v) {{")
            fid = 0
            for f in fields:
                w(f'  v.field({fid}, "{f["name"]}", e.{ident(f["name"])});')
                fid += 1
            cid = 0
            for c in child_kinds:
                if c.get("union") is not None:
                    w(
                        f'  v.union_child({cid}, "{c["name"]}", '
                        f'e.{ident(c["name"])});'
                    )
                else:
                    w(f'  v.child({cid}, "{c["name"]}", e.{ident(c["name"])});')
                cid += 1
            w("  (void)v;")
            w("}")
            w("")

    w("// Struct (variant-arm) visitors.")
    for st in s.structs:
        emit_struct_visit(st["name"], st["fields"], [])

    w("// Union item dispatch: visit the active member element.")
    for u in s.unions:
        for const in ("", "const "):
            w(f"template <class V> void Visit({const}{u['name']}& a, V&& v) {{")
            w("  std::visit(")
            w("      [&](auto& p) {")
            w("        if (p) Visit(*p, v);")
            w("      },")
            w("      a.node);")
            w("}")
            w("")

    w("// Element visitors.")
    for e in s.elements:
        emit_struct_visit(e["name"], e["fields"], e["children"])

    w("}  // namespace ps::mjcf")
    w("")
    w("#endif  // PROTOSPEC_GENERATED_VISIT_H")
    return "\n".join(o) + "\n"


def emit_reflect_h(s: Schema) -> str:
    o: list[str] = []
    w = o.append
    w(BANNER)
    w("//")
    w("// Runtime reflection: static schema description usable without")
    w("// compile-time knowledge of the element structs. This is the contract")
    w("// downstream UE detail-panel mirroring and the pybind layer consume.")
    w("//")
    w("// Shape: every element has an ElementDescriptor carrying its name, XML")
    w("// tag, a contiguous FieldDescriptor[] (one per authored field, in")
    w("// flattened schema order -- the same order and ids the generated Visit")
    w("// uses) and a ChildDescriptor[] (element or union child lists). Each")
    w("// FieldDescriptor states the field's storage kind, element type name")
    w("// (enum/ref-target/variant name for non-primitives), arity bounds, and")
    w("// whether it is optional / has an IDL default. Dynamic value access is")
    w("// the visitor's job (visit.h); reflection adds only presence + clear")
    w("// accessors (present/clear take the element by void* plus a field id).")
    w("// UnionDescriptor lists a union's member element types so a union child")
    w("// list can be walked generically. kFieldCount_<Element> constants pin")
    w("// each table's length for the generated-code self-check.")
    w("#ifndef PROTOSPEC_GENERATED_REFLECT_H")
    w("#define PROTOSPEC_GENERATED_REFLECT_H")
    w("")
    w("#include <cstddef>")
    w("#include <string_view>")
    w("")
    w('#include "types.h"')
    w("")
    w("namespace ps::mjcf::reflect {")
    w("")
    w("enum class FieldKind {")
    w("  Bool, Int32, Uint64, Float, Double, String, Enum, Ref, Variant,")
    w("};")
    w("")
    w("enum class ArityKind { Scalar, Fixed, Range, Unbounded };")
    w("")
    w("enum class Cardinality { ZeroOrMore, ZeroOrOne, One };")
    w("")
    w("struct FieldDescriptor {")
    w("  std::string_view name;       // IDL / IR field name")
    w("  std::string_view xml;        // MJCF attribute name")
    w("  std::string_view type_name;  // prim / enum / ref-target / variant name")
    w("  FieldKind kind;")
    w("  ArityKind arity;")
    w("  int arity_min;")
    w("  int arity_max;")
    w("  bool optional;")
    w("  bool has_default;")
    w("};")
    w("")
    w("struct ChildDescriptor {")
    w("  std::string_view name;    // IR-side child-list field name")
    w("  std::string_view target;  // element name, or union name if is_union")
    w("  bool is_union;")
    w("  Cardinality card;")
    w("};")
    w("")
    w("struct ElementDescriptor {")
    w("  std::string_view name;")
    w("  std::string_view xml;")
    w("  ElementType type;")
    w("  const FieldDescriptor* fields;")
    w("  std::size_t field_count;")
    w("  const ChildDescriptor* children;")
    w("  std::size_t child_count;")
    w("  bool (*present)(const void* elem, int field_id);")
    w("  void (*clear)(void* elem, int field_id);")
    w("};")
    w("")
    w("struct UnionDescriptor {")
    w("  std::string_view name;")
    w("  const ElementType* members;")
    w("  std::size_t member_count;")
    w("};")
    w("")
    w("// Field-count constants (pin each generated table's length).")
    for e in s.elements:
        w(
            f"inline constexpr std::size_t kFieldCount_{e['name']} = "
            f"{len(e['fields'])};"
        )
    w("")
    w("const ElementDescriptor& Describe(ElementType type);")
    w("const ElementDescriptor* DescribeByName(std::string_view name);")
