"""ProtoSpec C++ emitters: schema AST -> generated C++ object model.

This module is milestone 2. It consumes ``parse_spec("schema/mujoco.spec")``'s
canonical JSON AST (the contract documented in :mod:`protospec_gen.idl`) and
emits the generated half of the object model into ``cpp/generated/``:

    types.h      element structs, enums, POD structs, variant aliases, union
                 child-list item wrappers, the ElementType enum, and the
                 Clone/operator== declarations.
    types.cc     deep Clone() and field-wise operator== definitions.
    visit.h      the templated per-element/struct/union Visit(elem, V&&) hook
                 (plan Section 3's Serialize hook); field-id + child traversal.
    reflect.h    field/child/element/union descriptor tables (the downstream UE
                 mirroring + pybind contract; shape documented in that header).
    reflect.cc   the descriptor table storage and presence/clear thunks.
    keywords.h   enum <-> MJCF keyword string conversion declarations.
    keywords.cc  the keyword tables.
    defaults.h   ApplyDefault(elem) declarations.
    defaults.cc  the IDL `=` defaults applied field-by-field.
    xml_binding.h  per-element MJCF binding table types (attr/variant/child rows).
    xml_binding.cc the binding tables the handwritten reader/writer drive.

Representation decisions (see the plan's DR-1..DR-11):
  * presence: optional element fields are ``ps::opt<T>``; required fields are
    plain ``T``; struct (POD-cluster) fields are always plain (no identity, no
    presence -- the enclosing variant already encodes the authored form).
  * arrays: fixed ``[N]`` -> ``std::array<T,N>``; range ``[lo..hi]`` ->
    ``ps::InlineVec<T,hi>`` (capacity + filled count); unbounded ``[]`` ->
    ``std::vector<T>``; enum keyword sets (``Enum[]``) -> ``std::vector<Enum>``.
  * refs: ``ps::Ref<Target>`` holding the name string (Target is the element or
    union type, kept as a phantom for type-aware validation/rename).
  * variants: a ``std::variant`` alias over the arm types; the field is
    ``ps::opt<VariantName>`` (exactly one authored form, or none).
  * child lists: ``std::vector<std::unique_ptr<Child>>`` (stable identity);
    union child lists: one ordered ``std::vector<UnionAny>`` whose item wrapper
    holds a ``std::variant<std::unique_ptr<Member>...>`` (stable element
    pointers, interleave preserved).
  * identity: every element carries ``ps::SourceLoc loc`` and a monotonic
    ``std::uint64_t serial`` (fresh at construction; Clone re-mints so a clone
    attached into the same Model never duplicates a serial). Neither
    participates in ``operator==`` (they are provenance/identity, not content).

Entry point: ``python -m protospec_gen.emit`` writes the files;
``python -m protospec_gen.emit --check`` regenerates in memory and verifies the
checked-in files are byte-identical (same convention as draft_schema.py).
"""

