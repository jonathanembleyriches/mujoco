def emit_reflect_cc(s: Schema) -> str:
    o: list[str] = []
    w = o.append
    w(BANNER)
    w('#include "reflect.h"')
    w("")
    w("#include <array>")
    w("#include <iterator>")
    w("#include <string_view>")
    w("#include <unordered_map>")
    w("")
    w("namespace ps::mjcf::reflect {")
    w("namespace {")
    w("")

    # Per-element field tables.
    for e in s.elements:
        name = e["name"]
        fields = e["fields"]
        if fields:
            w(f"constexpr FieldDescriptor kFields_{name}[] = {{")
            for f in fields:
                ak, amin, amax = field_arity(f["type"])
                w(
                    f'    {{"{f["name"]}", "{s.field_xml(f)}", '
                    f'"{field_type_name(f["type"])}", '
                    f"FieldKind::{field_kind(f['type'])}, "
                    f"ArityKind::{ak}, {amin}, {amax}, "
                    f"{'true' if f['optional'] else 'false'}, "
                    f"{'true' if 'default' in f else 'false'}}},"
                )
            w("};")
        if e["children"]:
            w(f"constexpr ChildDescriptor kChildren_{name}[] = {{")
            for c in e["children"]:
                is_union = c.get("union") is not None
                target = c["union"] if is_union else c["element"]
                w(
                    f'    {{"{c["name"]}", "{target}", '
                    f"{'true' if is_union else 'false'}, "
                    f"Cardinality::{_CARDINALITY_CPP[c['cardinality']]}}},"
                )
            w("};")
        w("")

    # Presence + clear thunks.
    for e in s.elements:
        name = e["name"]
        w(f"bool Present_{name}(const void* p, int fid) {{")
        if not e["fields"]:
            w("  (void)p;")
            w("  (void)fid;")
            w("  return false;")
        else:
            w(f"  const auto& e = *static_cast<const {name}*>(p);")
            w("  (void)e;")
            w("  switch (fid) {")
            for i, f in enumerate(e["fields"]):
                m = ident(f["name"])
                if f["optional"]:
                    w(f"    case {i}: return e.{m}.has_value();")
                else:
                    w(f"    case {i}: return true;")
            w("    default: return false;")
            w("  }")
        w("}")
        optional_ids = [i for i, f in enumerate(e["fields"]) if f["optional"]]
        w(f"void Clear_{name}(void* p, int fid) {{")
        if not optional_ids:
            w("  (void)p;")
            w("  (void)fid;")
        else:
            w(f"  auto& e = *static_cast<{name}*>(p);")
            w("  switch (fid) {")
            for i in optional_ids:
                m = ident(e["fields"][i]["name"])
                w(f"    case {i}: e.{m}.reset(); break;")
            w("    default: break;")
            w("  }")
        w("}")
        w("")

    # Union member tables.
    for u in s.unions:
        members = ", ".join(f"ElementType::{m}" for m in u["members"])
        w(f"constexpr ElementType kUnionMembers_{u['name']}[] = {{ {members} }};")
    w("")

    # Element descriptor table.
    w("constexpr ElementDescriptor kElements[] = {")
    for e in s.elements:
        name = e["name"]
        fields = e["fields"]
        children = e["children"]
        fptr = f"kFields_{name}" if fields else "nullptr"
        cptr = f"kChildren_{name}" if children else "nullptr"
        w(
            f'    {{"{name}", "{s.elem_xml(e)}", ElementType::{name}, '
            f"{fptr}, {len(fields)}, {cptr}, {len(children)}, "
            f"&Present_{name}, &Clear_{name}}},"
        )
    w("};")
    w("")
    w("constexpr UnionDescriptor kUnions[] = {")
    for u in s.unions:
        w(
            f'    {{"{u["name"]}", kUnionMembers_{u["name"]}, '
            f"{len(u['members'])}}},"
        )
    w("};")
    w("")
    w("const std::unordered_map<std::string_view, const ElementDescriptor*>&")
    w("NameIndex() {")
    w("  static const std::unordered_map<std::string_view,")
    w("                                  const ElementDescriptor*>")
    w("      index = [] {")
    w("        std::unordered_map<std::string_view, const ElementDescriptor*> m;")
    w("        for (const auto& d : kElements) m.emplace(d.name, &d);")
    w("        return m;")
    w("      }();")
    w("  return index;")
    w("}")
    w("")
    w("}  // namespace")
    w("")
    w("const ElementDescriptor& Describe(ElementType type) {")
    w("  return kElements[static_cast<std::size_t>(type)];")
    w("}")
    w("")
    w("const ElementDescriptor* DescribeByName(std::string_view name) {")
    w("  const auto& index = NameIndex();")
    w("  auto it = index.find(name);")
    w("  return it == index.end() ? nullptr : it->second;")
    w("}")
    w("")
    w("std::size_t ElementCount() { return std::size(kElements); }")
    w("")
    w("const ElementDescriptor& ElementAt(std::size_t index) {")
    w("  return kElements[index];")
    w("}")
    w("")
    w("const UnionDescriptor& DescribeUnion(std::string_view name) {")
    w("  for (const auto& u : kUnions)")
    w("    if (u.name == name) return u;")
    w("  return kUnions[0];")
    w("}")
    w("")
    w("std::size_t UnionCount() { return std::size(kUnions); }")
    w("")
    w("const UnionDescriptor& UnionAt(std::size_t index) {")
    w("  return kUnions[index];")
    w("}")
    w("")
    w("}  // namespace ps::mjcf::reflect")
    return "\n".join(o) + "\n"


