def _load(schema=None, mjs=None):
    if schema is None:
        schema = parse_spec(SCHEMA).to_json()
    if mjs is None:
        with open(FIELDS_JSON, "r", encoding="utf-8") as fh:
            mjs = json.load(fh)
    return schema, mjs


def generate_files(schema=None, mjs=None):
    """Return {filename: content} for the generated mjs binding files. Raises
    MjsCoverageError on any coverage gap or orphaned table entry."""
    schema, mjs = _load(schema, mjs)
    dispositions = _element_dispositions(schema, mjs)
    return {
        "mjs_binding.h": emit_binding_h(dispositions),
        "mjs_binding.cc": emit_binding_cc(dispositions),
    }


def stats(schema=None, mjs=None):
    """Coverage statistics for reporting/tests: per-element (exact, aliased,
    waived, name, variant) counts, plus element mapped/waived totals."""
    schema, mjs = _load(schema, mjs)
    dispositions = _element_dispositions(schema, mjs)
    per = {}
    for name, rows in dispositions.items():
        c = {"exact": 0, "alias": 0, "waive": 0, "name": 0, "variant": 0}
        for _f, disp in rows:
            c[disp[0]] += 1
        per[name] = c
    return {
        "mapped_elements": len(ELEMENT_STRUCT),
        "waived_elements": len(ELEMENT_WAIVERS),
        "per_element": per,
    }
