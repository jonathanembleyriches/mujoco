def _cast_ctype(m):
    """The mjs scalar field's C ctype (for enum static_cast)."""
    return m["ctype"]


def _emit_field(w, f, disp):
    """Emit the conversion for one field (already guarded on presence below)."""
    name = f["name"]
    t = f["type"]
    optional = f["optional"]
    val = f"(*e.{name})" if optional else f"e.{name}"
    guard = f"e.{name}.has_value()" if optional else None
    kind = disp[0]

    def line(body):
        if guard:
            w(f"  if ({guard}) {{ {body} }}")
        else:
            w(f"  {body}")

    if kind == "name":
        line(f"mjs_setName(out->element, {val}.c_str());")
        return
    if kind == "variant":
        line(f"ps::mjcf::compile::{disp[1]}({val}, out);")
        return

    if kind == "waive":
        return
    m = disp[1]  # exact / alias -> mjs field dict
    mk = m["kind"]
    mn = m["name"]
    shape = ps_shape(t)

    if shape in ("scalar",):
        line(f"out->{mn} = {val};")
    elif shape == "enum_scalar":
        line(f"out->{mn} = static_cast<{_cast_ctype(m)}>(ToMjt({val}));")
    elif shape in ("string",):
        line(f"mjs_setString(out->{mn}, {val}.c_str());")
    elif shape == "ref":
        line(f"mjs_setString(out->{mn}, {val}.name.c_str());")
    elif shape == "ref_list":
        line(f"ps::mjcf::compile::SetRefVec(out->{mn}, {val});")
    elif shape == "fixed":
        n = t["arity"]["size"]
        line(f"for (int i = 0; i < {n}; ++i) out->{mn}[i] = {val}[i];")
    elif shape == "range":
        line(f"for (std::size_t i = 0; i < {val}.size(); ++i) "
             f"out->{mn}[i] = {val}[i];")
    elif shape == "vec":
        helper = {"double_vec": "SetDouble", "int_vec": "SetInt",
                  "float_vec": "SetFloat"}[mk]
        line(f"ps::mjcf::compile::{helper}(out->{mn}, {val});")
    else:  # pragma: no cover -- guarded by compatible()
        raise MjsCoverageError(f"unhandled shape {shape} for {name}")


def emit_binding_h(dispositions):
    o = []
    w = o.append
    w(BANNER)
    w("//")
    w("// ApplyMjs(const <Element>&, mjs<Struct>*): write authored fields only.")
    w("// Compiled solely by the MuJoCo-linked compile target (never Tier-0).")
    w("#ifndef PROTOSPEC_GENERATED_MJS_BINDING_H")
    w("#define PROTOSPEC_GENERATED_MJS_BINDING_H")
    w("")
    w("#include <mujoco/mujoco.h>  // mjSpec structs + mjs_set* setter surface")
    w("")
    w('#include "types.h"')
    w("")
    w("namespace ps::mjcf {")
    w("")
    for name in sorted(dispositions):
        struct = ELEMENT_STRUCT[name]
        w(f"void ApplyMjs(const {name}& e, {struct}* out);")
    w("")
    w("}  // namespace ps::mjcf")
    w("")
    w("#endif  // PROTOSPEC_GENERATED_MJS_BINDING_H")
    return "\n".join(o) + "\n"


def emit_binding_cc(dispositions):
    o = []
    w = o.append
    w(BANNER)
    w('#include "mjs_binding.h"')
    w("")
    w("#include <cstddef>")
    w("")
    w('#include "mjs_convert.h"')
    w("")
    w("namespace ps::mjcf {")
    w("namespace {")
    w("")

    # ToMjt overloads, one per enum actually used.
    used_enums = _enums_in_use(dispositions)
    for enum in sorted(used_enums):
        ctype, members = ENUM_MJT[enum]
        w(f"int ToMjt({enum} v) {{")
        w("  switch (v) {")
        for member, const in members.items():
            w(f"    case {enum}::{ident(member)}: return {const};")
        w("    default: return 0;")
        w("  }")
        w("}")
        w("")
    w("}  // namespace")
    w("")

    for name in sorted(dispositions):
        struct = ELEMENT_STRUCT[name]
        w(f"void ApplyMjs(const {name}& e, {struct}* out) {{")
        for f, disp in dispositions[name]:
            _emit_field(w, f, disp)
        w("  (void)e;")
        w("  (void)out;")
        w("}")
        w("")

    w("}  // namespace ps::mjcf")
    return "\n".join(o) + "\n"


def _enums_in_use(dispositions):
    enums = set()
    for rows in dispositions.values():
        for f, disp in rows:
            if disp[0] in ("exact", "alias") and ps_shape(f["type"]) == "enum_scalar":
                enums.add(f["type"]["name"])
    return enums


